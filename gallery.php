  <script>
    // Gallery scripts
    document.addEventListener('DOMContentLoaded', () => {
      // Filtering
      const filterBtns = document.querySelectorAll('.filter-btn');
      const galleryItems = document.querySelectorAll('.gallery-item');

      filterBtns.forEach(btn => {
        btn.addEventListener('click', () => {
          // Update active button
          filterBtns.forEach(b => {
            b.classList.remove('bg-amber-700', 'text-white');
            b.classList.add('bg-[#e5dfd8]', 'text-stone-700');
          });
          btn.classList.remove('bg-[#e5dfd8]', 'text-stone-700');
          btn.classList.add('bg-amber-700', 'text-white');

          const filter = btn.getAttribute('data-filter');

          galleryItems.forEach(item => {
            if (filter === 'all' || item.getAttribute('data-category') === filter) {
              item.classList.remove('hidden');
              item.classList.add('visible');
            } else {
              item.classList.remove('visible');
              item.classList.add('hidden');
            }
          });
        });
      });
    });

    const lightbox = document.getElementById('lightbox');
    const lightboxImg = document.getElementById('lightbox-img');
    const lightboxTitle = document.getElementById('lightbox-title');
    const lightboxDesc = document.getElementById('lightbox-desc');

    function openLightbox(url, title, desc) {
      lightboxImg.src = url;
      lightboxTitle.innerText = title;
      lightboxDesc.innerText = desc;
      lightbox.classList.remove('hidden');
    }

    function closeLightbox() {
      lightbox.classList.add('hidden');
    }

    // Close on click outside
    lightbox.addEventListener('click', (e) => {
      if (e.target === lightbox) {
        closeLightbox();
      }
